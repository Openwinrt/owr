namespace System
{
    public class OutOfMemoryException : Exception
    {
        public OutOfMemoryException()
        {
            base..ctor();
            return;
        }

        public OutOfMemoryException(string message)
        {
            base..ctor();
            return;
        }

        public OutOfMemoryException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

