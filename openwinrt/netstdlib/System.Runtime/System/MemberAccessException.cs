namespace System
{
    public class MemberAccessException : Exception
    {
        public MemberAccessException()
        {
            base..ctor();
            return;
        }

        public MemberAccessException(string message)
        {
            base..ctor();
            return;
        }

        public MemberAccessException(string message, Exception inner)
        {
            base..ctor();
            return;
        }
    }
}

