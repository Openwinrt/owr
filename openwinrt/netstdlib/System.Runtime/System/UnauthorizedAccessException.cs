namespace System
{
    public class UnauthorizedAccessException : Exception
    {
        public UnauthorizedAccessException()
        {
            base..ctor();
            return;
        }

        public UnauthorizedAccessException(string message)
        {
            base..ctor();
            return;
        }

        public UnauthorizedAccessException(string message, Exception inner)
        {
            base..ctor();
            return;
        }
    }
}

