namespace System
{
    public class FormatException : Exception
    {
        public FormatException()
        {
            base..ctor();
            return;
        }

        public FormatException(string message)
        {
            base..ctor();
            return;
        }

        public FormatException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

