namespace System
{
    public class OverflowException : ArithmeticException
    {
        public OverflowException()
        {
            base..ctor();
            return;
        }

        public OverflowException(string message)
        {
            base..ctor();
            return;
        }

        public OverflowException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

