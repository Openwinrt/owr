namespace System
{
    public class InvalidTimeZoneException : Exception
    {
        public InvalidTimeZoneException()
        {
            base..ctor();
            return;
        }

        public InvalidTimeZoneException(string message)
        {
            base..ctor();
            return;
        }

        public InvalidTimeZoneException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

