namespace System
{
    public class NotSupportedException : Exception
    {
        public NotSupportedException()
        {
            base..ctor();
            return;
        }

        public NotSupportedException(string message)
        {
            base..ctor();
            return;
        }

        public NotSupportedException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

