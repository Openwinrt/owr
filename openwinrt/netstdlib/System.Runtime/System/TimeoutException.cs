namespace System
{
    public class TimeoutException : Exception
    {
        public TimeoutException()
        {
            base..ctor();
            return;
        }

        public TimeoutException(string message)
        {
            base..ctor();
            return;
        }

        public TimeoutException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

