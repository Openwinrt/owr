namespace System
{
    public class NotImplementedException : Exception
    {
        public NotImplementedException()
        {
            base..ctor();
            return;
        }

        public NotImplementedException(string message)
        {
            base..ctor();
            return;
        }

        public NotImplementedException(string message, Exception inner)
        {
            base..ctor();
            return;
        }
    }
}

