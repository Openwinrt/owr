namespace System
{
    public class InvalidOperationException : Exception
    {
        public InvalidOperationException()
        {
            base..ctor();
            return;
        }

        public InvalidOperationException(string message)
        {
            base..ctor();
            return;
        }

        public InvalidOperationException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

