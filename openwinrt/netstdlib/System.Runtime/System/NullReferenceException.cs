namespace System
{
    public class NullReferenceException : Exception
    {
        public NullReferenceException()
        {
            base..ctor();
            return;
        }

        public NullReferenceException(string message)
        {
            base..ctor();
            return;
        }

        public NullReferenceException(string message, Exception innerException)
        {
            base..ctor();
            return;
        }
    }
}

